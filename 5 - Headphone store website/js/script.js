function openNav() {
    document.getElementById("mySidenav").style.width = "100%";
    document.getElementById("icon-user").style.display = "inline";
    document.getElementById("btn-into").style.display = "inline";
}

function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementsByClassId("remove-i").style.display = "none";
}
